      const companyData = {
        id: nanoid(),
        name: company.name,
        industry: company.industry,
        location: company.location,
        country: company.country,
        website: company.website,
        employeeCount: company.headcount.toLocaleString(), // Convert number to string format
        size: company.headcount >= 1000000 ? "Enterprise (1M+)" : 
              company.headcount >= 100000 ? "Large (100K+)" :
              company.headcount >= 10000 ? "Medium (10K+)" : "Small (<10K)",
        lastUpdate: new Date(),
        status: "safe"
      };
      
      await db.insert(companies).values(companyData);
      console.log(`âœ“ Added ${company.name} - ${company.headcount.toLocaleString()} employees`);
